package checkers;

public class Coordinates {

	int locX;
	int locY;
	
	public Coordinates(int x, int y) {
		this.locX = x;
		this.locY = y;
	}
	
	public int getLocX() {
		return locX;
	}
	public void setLocX(int locX) {
		this.locX = locX;
	}
	public int getLocY() {
		return locY;
	}
	public void setLocY(int locY) {
		this.locY = locY;
	}
	
}
